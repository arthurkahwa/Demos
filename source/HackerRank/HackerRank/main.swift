import Foundation

func jumpingOnClouds(c: [Int]) -> Int {
    var jumps = 0
    var n = 0

    while n < c.lastIndex(of: 0)! {
        if c[n + 1] == 0 {
            n += 1
            jumps += 1

            if n >= c.lastIndex(of: 0)! { break }

            if c[n + 1] == 0 {
                n += 1
                // jumps += 1
            }
        }
        else if c[n + 1] == 1 {
            n += 2
            jumps += 1
        }
    }

    return jumps
}

